fn main() {
   let mut i = 100;
   let mut f = 33.333;
   println!("the current value of i is {} and f is {}", i, f);
   i += 50;
   f += 6.667;
   println!("the current value of i is {} and f is {}", i, f);
   i -= 50;
   f -= 10;
   println!("the current value of i is {} and f is {}", i, f);
   i *= 2;
   f *= 2.5;
   println!("the current value of i is {} and f is {}", i, f);
   i /= 2;
   f /= 2.5;
   println!("the current value of i is {} and f is {}", i, f);
}
